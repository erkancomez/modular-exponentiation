    public void pozitifkalanlar(int ali, int kuv, int be, int mod, int ussayisi) {
        int adet = 0;
        String ikiliksayi = String.valueOf(ali);
        String[] parca = ikiliksayi.split("");
        int[] array;
        array = new int[15];
        double[] array2;

        for (int i = 0; i < ikiliksayi.length(); i++) {
            if (parca[i].equals("1")) {
                adet++;
            }
        }

        array2 = new double[adet];
        int p = 0;
        int y = 0;
        double hh = array2.length;
jTextArea1.append("\n\n");
        System.out.print(be + "^" + ussayisi + " = ");
        jTextArea1.append(be + "^" + ussayisi + " = ");
        
        while (ali > 0) {

            array[p] = ali % 10;
            p++;
            ali = ali / 10;

        }

        for (int j = 0; j < array.length; j++) {

            if (array[j] == 1) {

                double pk = Math.pow(2, j);
                double kpk = Math.pow(be, pk);
                //System.out.println(kpk);
                array2[y] = (double) kpk;


                   // System.out.print(array2[y] + " . ");

                    jTextArea1.append(array2[y] + " * ");
                
                // mod alıyoruz
                y++;

            }

        }

        carp(array2, hh, mod);

    }

    public double carp(double array2[], double hh, int mod) {
        double result = 1;
        for (int i = 0; i < hh; i++) {
            result = result * array2[i];
        }

        jTextArea1.append("\n");
        double a = result % mod;
        // System.out.print("= " + result + " = ");
        //  System.out.println(a + "(mod " + mod + ")");
        jTextArea1.append("= " + result + " = ");
        jTextArea1.append((int)a + "(mod " + mod + ")");
        return result;
    }

