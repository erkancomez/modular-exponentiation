    public void binaryDonustur(int sayi) {

        int x;
        int ussayisi = sayi;
        String string = "";

        while (sayi > 0) {

            x = sayi % 2;
            string = x + string;
            sayi = sayi / 2;

        }

        jTextField4.setText(string);
        int binary = Integer.parseInt(string);

        //System.out.println("N sayısının Binary karşılığı: " + binary);

        basamakogren(binary, ussayisi);

    }   //Dönüştürme bitti.

    public void basamakogren(int num, int ussayisi) {
        int bin = num;
        int k = 0;

        while (num != 0) {
            // num = num/10
            num /= 10;
            ++k;
        }
        //  System.out.println("k sayısı: " + k);
        k = k - 1;

        usvemod(k, bin, ussayisi);
    }

